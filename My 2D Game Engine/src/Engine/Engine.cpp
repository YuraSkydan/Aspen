void Engine::SetCurrentTheme()
{
	switch (m_CurrentTheme)
	{
	case Theme::Dark:
		DarkStyle();
		break;
	case Theme::Black:
		BlackStyle();
		break;
	case Theme::CorporateGray:
		CorporateGreyStyle();
		break;
	case Theme::DarkNight:
		DarkNightStyle();
		break;
	case Theme::Light:
		ImGui::StyleColorsLight();
		break;
	}
}

void Engine::ImGuiMainMenuBar()
{
	ImGui::StyleColorsLight();
	if (ImGui::BeginMainMenuBar())
	{
		ImGui::SetWindowSize(ImVec2(1080, 100));
		if (ImGui::BeginMenu("File"))
		{
			if (ImGui::MenuItem("New Scene"));
			if (ImGui::MenuItem("Open Scene"));
			ImGui::Separator();

			if (ImGui::MenuItem("Save"));
			if (ImGui::MenuItem("Save As..."));
			ImGui::Separator();

			if (ImGui::MenuItem("New Project"));
			if (ImGui::MenuItem("Open Project"));
			if (ImGui::MenuItem("Save Project"));
			ImGui::EndMenu();
		}

		if (ImGui::BeginMenu("Edit"))
		{
			ImGui::EndMenu();
		}

		if (ImGui::BeginMenu("Gamebject"))
		{
			ImGui::EndMenu();
		}

		if (ImGui::BeginMenu("Options"))
		{
			if (ImGui::BeginMenu("Themes"))
			{
				if (ImGui::MenuItem("Black Style"))
					m_CurrentTheme = Theme::Black;
				if (ImGui::MenuItem("Corporate Grey Style"))
					m_CurrentTheme = Theme::CorporateGray;
				if (ImGui::MenuItem("Dark Style"))
					m_CurrentTheme = Theme::Dark;
				if (ImGui::MenuItem("Dark Night Style"))
					m_CurrentTheme = Theme::DarkNight;
				if (ImGui::MenuItem("Light Style"))
					m_CurrentTheme = Theme::Light;

				ImGui::EndMenu();
			}
			ImGui::EndMenu();
		}

		ImGui::EndMainMenuBar();
	}

	SetCurrentTheme();
}


Engine::Engine()
{
	Log::Init(); // maybe move it somewhere else

	INFO("Engine Start");

	s_Instance = this;
	m_Window = Window::Create();
	m_Editor = std::make_unique<Editor>();

	InitImGui();
}

void Engine::Run()
{
	while (m_Running)
	{
		glClear(GL_COLOR_BUFFER_BIT);

		ImGuiBegin();

		ImGuiMainMenuBar();

		m_Editor->Update();
		m_Editor->ImGuiRender();

		ImGuiEnd();
		
		m_Window->Update();
		glfwPollEvents();
		//change latter
		m_Running = !glfwWindowShouldClose(m_Window->GetNativeWindow());
	}
}

void Engine::Close()
{
	m_Running = false;
}
